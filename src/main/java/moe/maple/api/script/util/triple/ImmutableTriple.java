package moe.maple.api.script.util.triple;

import moe.maple.api.script.util.tuple.ImmutableTuple;

public class ImmutableTriple<L, C, R> extends ImmutableTuple<L, R> implements Triple<L, C, R> {

    private final C center;

    public ImmutableTriple(L left, C center, R right) {
        super(left, right);
        this.center = center;
    }

    @Override
    public C center() { return center; }

}
